        ni = len(self.point)-1
        nj = len(self.point[0])-1
        nk = len(self.point[0][0])-1
        #triangles = []
        point = self.point
        for i in xrange(0,ni):
            for j in xrange(0,nj):
                for k in xrange(0,nk):
                    p = self.GridCell()
                    position = p.position
                    (x,y,z)=(float(i),float(j),float(k))
                    position[0] = (x,y,z)
                    position[1] = (x+1,y,z)
                    position[2] = (x+1,y+1,z)
                    position[3] = (x,y+1,z)
                    position[4] = (x,y,z+1)
                    position[5] = (x+1,y,z+1)
